package pack2;

public class A1 {
public static void main(String[] args) {
	
	int len=5;
	for(int i=1;i<=len;i++) {
		for(int j=i;j<=len;j++) {
			System.out.print("*");
		}
		System.out.println();
	}
	for(int x=2;x<=len;x++) {
	for(int k=1;k<=x;k++) {
		System.out.print("*");
	}
	System.out.println();
}
	}}

